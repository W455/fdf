#include "libft.h"

void	*ft_memccpy(void *s1, const void *s2, int c, size_t n)
{
	unsigned char	*p1;
	unsigned char	*p2;

	p1 = (unsigned char*)s1;
	p2 = (unsigned char*)s2;
	if (n)
	{
		while (n--)
			if ((*p1++ = *p2++) == (unsigned char)c)
				return (p1);
	}
	return (NULL);
}
